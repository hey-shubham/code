// Write a program to check whether a number is even or odd using if-else statement.
#include<iostream>
using namespace std;

int main()
{
    system("cls");
    int n;
    cout<<"enter any number: ";
    cin>>n;
    if(n%2==0)
        {
        cout<<"it is a even number."<<endl;
        }
    else
        {
        cout<<"it is a odd number."<<endl;
        }
};